								<p>Vi är så många som inte får någon hjälp med träning eller rehabilitering efter förlossning. Mycket fokus hamnar på barnet i början, och ingen eller lite tid läggs på sig själv och sina egna kropp. Det är här <b>Mommy Academy</b> kommer in och fyller ett viktigt tomrum, en chans att få fokusera på sig själv, sin kropp och att få bli stark för att orka med den nya vardagen. För om du är skapad för att röra dig på ett visst sätt, och du rör dig på ett annat, så börjar kroppen att kompensera och då uppstår överbelastning/skador/smärtor på olika ställen i kroppen. Så det logiska borde vara att återskapa det normala rörelsemönstret, och det är just det som Mommy Academy handlar om, att normalisera ett rörelsemönster, så att vi kan ta oss framåt på ett så energisnålt sätt som möjligt.</p>
								<p>Detta kommer att bli en investering som skall hålla på lång sikt, ingen “quick fix” eller tillfällig lösning. Men det kommer att kräva tålamod, mycket slit och självdisciplin. Har du viljan och motivationen så står vi för kunskapen och resurserna. Tillsammans kan vi åstadkomma väldigt bra resultat.</p>
								<p>Antalet kursdeltagare under denna kurs kommer alltid att vara få, max 10 personer, då man skall känna att man står i fokus och får den hjälp/coaching man behöver. Det är en av skillnaderna att komma till oss eller att gå till gymmets mammagrupper. Vid ett av tillfällena kommer vi också att ha besök av Leg. Barnmorska som kommer att svara på frågor och funderingar kring mammakroppen.</p>
