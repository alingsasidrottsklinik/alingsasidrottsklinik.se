								<h3 class="ui header">Om Alingsås Rygg & Idrottsklinik</h3>
								<p>Vi på Alingsås Rygg & Idrottsklinik tror på att jobba i team med olika kompetensområden, för att nå bästa, snabbaste och mest ihållande resultat.</p>
								<p>Idag är vi två Legitimerade Naprapater, en Legitimerad Läkare, Näringsterapeut samt Yogalärare. Vi har även ett nära samarbete med Psykiatriker, KBT-terapeut samt duktiga Massageterapeuter.</p>
								<p>Oavsett om vi är rätt personer att hjälpa dig så ansvarar vi för att våra patienter slussas vidare till rätt instans. För oss är det viktigt att kunna hjälpa människor till ett mer hälsosamt liv, oavsett om det är vi som utför arbetet eller ej.</p>
								<p>Vi hjälper dig med dina rygg, led och muskelbesvär och inriktar oss på idrottsutövare på både motions- och elitnivå men behandlar även vardags/arbetsrelaterade besvär.</p>
								<p>Oavsett om du har ett akut problem eller har haft problem under längre tid genomför vi alltid en utförlig undersökning för att ställa en så korrekt diagnos som möjligt. Vi tillämpar en undersökningsmetodik bland annat baserad på neurologisk status, rörelsetester samt specifika ortopediska tester för att hitta grundorsaken till symptomen.</p>
								<p>När det akuta läget är avhjälpt har vi då möjligheten att jobba med förebyggande behandling.</p>
								<p>Vanliga diagnoser som vi behandlar:</p>
								<ul>
									<li>Nackspärr</li>
									<li>Diskbråck</li>
									<li>Ländryggssmärta (Lumbago)</li>
									<li>Tennis-/golfarmbåge</li>
									<li>Impingement syndrome</li>
									<li>Huvudvärk</li>
									<li>Artros</li>
									<li>Löpar-/hopparknä</li>
									<li>Benhinneinflammation</li>
									<li>Hälsporre</li>
									<li>PFS (Patello-Femoral Syndrome)</li>
								</ul>
								<p>Vi arbetar även med elektroterapi så som <a href="http://se.skanlab.no/skanlab-therapy/" target="_blank" title="Läs mer om Skanlab">Skanlab</a>, <a href="http://se.skanlab.no/skanlab-laser-pro/" target="_blank" title="Läs mer om Skanlabs Laser">Laser</a> och <a href="http://se.skanlab.no/stotvag/" target="_blank" title="Läs mer om Skanlabs Stötvåg">Stötvåg</a>. Dessa tekniker möjliggör snabbare rehabilitering vid ett flertal olika diagnoser.</p>
